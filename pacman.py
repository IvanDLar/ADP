def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def sign(v):
    """Get Sign of value"""
    return 1 if v >= 0 else -1

def get_dir(current, target):
    """Gets the best direction for """
    dx = target.x - current.x
    dy = target.y - current.y
    x = sign(dx)
    y = sign(dy)
      
    if  dx >= dy:
        y =  0
    else:
        x = 0

    return vector(x,y)




def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course * modifier):
          point.move(course * modifier)
        else:
          dir = get_dir(point, pacman)
          print(dir)
          if valid(point + dir * modifier):
            course.x = dir.x
            course.y = dir.y
      
          else:           
            options = [
                vector(1, 0),
                vector(-1, 0),
                vector(0, 1),
                vector(0, -1),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for i in range(len(ghosts) - 1, -1,-1):
        if abs( pacman - ghosts[i][0]) < 20:
            ghosts.pop(i)
            print("POPPED")
            

    ontimer(move, 10)


def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

